# module_1_3 Тема динамическая типизация

# Переменная типа string
name = "Фаниль Шамильевич Габдуллин"
print(f"Мое имя: {name}")

# Переменная типа int
age = 61
print(f"Мой возраст: {age}")

# Изменение значения переменной age
age += 0
print(f"Теперь мой возраст: {age}")

# Переменная типа bool
is_student  = True
print(f"Я студент : {is_student}")
